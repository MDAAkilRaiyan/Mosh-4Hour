# String Lecture

# Use of Single Quote
single = 'Classes for "Beginners"\n'
print(single)

# Use of Double Quote
double = "Classes for 'Beginners'\n"
print(double)

# Use of Triple Quote
triple = ''' 
Hi,
I'm Aakil.
Have you seen "Superman(2025)" lately?
'''
print(triple)

# Printing Character Based on Index Numbers
line = "Once upon a time.....in Hollywood"
print(line[3] + " " + line[5])
print(line[-3] + " " + line[-5])
print(line[2:8])
print(line[1:-1])
