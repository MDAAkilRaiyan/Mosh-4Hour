import math
# We can use many math functions by importing the math module and use it by math.

# Functions to Help in Math Operations

# Round function to Round Numbers
x = 5.76
print(round(x))


# Absolute function to make any number positive
x = -345
print(abs(x))

# Ceil Method from the math module
# This method rounds a number up to the smallest integer greater than or equal to x
x = 8.394
print(math.ceil(x))

# Floor Method from the math module
# This method rounds a number down to the largest integer less than or equal to x
x = 2.234
print(math.floor(x))
